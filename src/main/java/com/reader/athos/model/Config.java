package com.reader.athos.model;

public class Config {
	
	private String targetReportsPath;
	private String folderPath;
	
	
	public String getTargetReportsPath() {
		return targetReportsPath;
	}
	
	public void setTargetReportsPath(String targetReportsPath) {
		this.targetReportsPath = targetReportsPath;
	}
	
	public String getFolderPath() {
		return folderPath;
	}
	
	public void setFolderPath(String folderPath) {
		this.folderPath = folderPath;
	}
}
