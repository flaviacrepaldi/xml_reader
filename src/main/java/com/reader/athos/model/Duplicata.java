package com.reader.athos.model;

public class Duplicata {
    private String nDup;
    private String dVenc;
    private String vDup;
    
    
	public Duplicata(String nDup, String dVenc, String vDup) {
		super();
		this.nDup = nDup;
		this.dVenc = dVenc;
		this.vDup = vDup;
	}


	public String getnDup() {
		return nDup;
	}


	public String getdVenc() {
		return dVenc;
	}


	public String getvDup() {
		return vDup;
	}


}
